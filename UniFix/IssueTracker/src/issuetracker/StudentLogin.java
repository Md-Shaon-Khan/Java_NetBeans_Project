    private void stu_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stu_loginActionPerformed
        String reg_no = jreg.getText().trim();
    String password = new String(jpassword.getPassword()).trim();

    if (reg_no.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Reg. No or Password cannot be empty!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
       // con = connect(); // Ensure connection is established
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Connection failed. Check your database settings.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String query = "SELECT * FROM student_signup WHERE reg_no = ? AND password = ?";
        pst = con.prepareStatement(query);
        pst.setString(1, reg_no);
        pst.setString(2, password);

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Login Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose(); // Close login window
            Student_Desk studentdesk = new Student_Desk(reg_no, rs.getString("full_name"), con);
            studentdesk.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Registration Number or Password!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        try {
            if (pst != null) pst.close();
            // DO NOT close `con` here, pass it to `Student_Desk`
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error closing connection: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_stu_loginActionPerformed

